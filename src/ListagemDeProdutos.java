//Crie uma listagem de produtos utilizando variáves primitivas

public class ListagemDeProdutos {
    public static void main(String[] args) {
        System.out.println("Listagem dos produtos");

        System.out.println("-".repeat(40));

        System.out.println("Nome: Livro");
        double precoLivro = 49.95;
        System.out.println("Preço: R$ " + precoLivro);

        System.out.println("-".repeat(40));

        System.out.println("Nome: Celular");
        double precoCelular = 1470.76;
        System.out.println("Preço: R$ " + precoCelular);

        System.out.println("-".repeat(40));
    }
}
